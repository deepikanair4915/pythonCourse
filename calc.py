num1=int(input("enter first number: "))
num2=int(input("enter second number: "))
oper=input("enter the operation: ")
if oper == "+":
    print(num1+num2)
elif oper== "-":
    print(num1-num2)
elif oper== "*":
    print(num1*num2)
elif oper== "/":
    print(num1/num2)
else :
    print ("not valid")
    
    
